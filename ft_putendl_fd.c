#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int len;

	if (fd < 0)
		return ;
	len = 0;
	if (s)
	{
		while (s[len] != '\0')
			len++;
		write(fd, s, len);
		write(fd, "\n", 1);
	}
}
