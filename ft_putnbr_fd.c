#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char i;

	if (fd < 0)
		return ;
	if (n < 0)
	{
		if (n == -2147483648)
			write(fd, "-2147483648", 11);
		else
		{
			write(fd, "-", 1);
			ft_putnbr_fd(-n, fd);
		}
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		i = n + '0';
		write(fd, &i, 1);
	}
}
